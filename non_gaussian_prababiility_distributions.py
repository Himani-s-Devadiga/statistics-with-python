# Statistical distributions help describe how data is spread in real-world scenarios like normal, log-normal, Pareto, and uniform distributions.
# Data transformations such as log, square root, reciprocal, Box–Cox, and Yeo–Johnson are used to reduce skewness and stabilize variance.
# These transformations help make data more suitable for statistical analysis and machine learning models.
# Kurtosis measures the shape of a distribution in terms of peak and tail heaviness, helping to identify outliers.
# Based on kurtosis, distributions are classified into mesokurtic, leptokurtic, and platykurtic types, which help understand data behavior better.



